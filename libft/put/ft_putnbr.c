#include "../libft.h"

void	ft_putnbr(int n)
{
	long	i;

	i = 1;
	if (n == -2147483648)
		ft_putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			ft_putchar('-');
			n = -n;
		}
		while (i <= n)
			i *= 10;
		while (i > 10)
		{
			i /= 10;
			ft_putchar((n / i) + '0');
			n = n % i;
		}
		ft_putchar((n % 10) + '0');
	}
}
